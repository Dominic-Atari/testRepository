package myfirstproject;

public class application {

	public static void main(String[] args) {
		//find the area of a Trapezium.
		int shorterSide = 5;
		int longerSide = 10;
		int hight = 7;
		
		// sum of a and b.
		int totalLength = shorterSide + longerSide;
		System.out.println(totalLength);
		
		// a+b * h.
		int lengthHight = totalLength * hight;
		
		//are 
		System.out.println(0.5 * lengthHight);
	}
}